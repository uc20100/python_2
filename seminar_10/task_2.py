class Rectangle:
    def __init__(self, length, width=None):
        self.length = length
        self.width = width

    def perimeter(self):
        if self.width is None:
            return 4 * self.length
        else:
            return 2 * (self.length + self.width)

    def area(self):
        if self.width is None:
            return self.length ** 2
        else:
            return self.length * self.width


